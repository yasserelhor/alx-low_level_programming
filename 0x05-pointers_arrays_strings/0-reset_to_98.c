/**
 * reset_to_98 - Resets the value of an integer pointer to 98.
 *
 * @n: Pointer to an integer to be reset.
 *
 * Description:
 * This function takes a pointer to an
 * integer as its parameter and sets the
 * value of the integer it points to, to 98.
 */

void reset_to_98(int *n)
{
	*n = 98;
}
