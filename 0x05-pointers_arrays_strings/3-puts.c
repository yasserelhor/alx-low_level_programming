#include "main.h"

/**
 * _puts - Prints a string to stdout followed by a newline
 * @str: The string to be printed
 *
 * Description:
 * This function takes a pointer to a string as an argument and prints
 * the string to the standard output followed by a newline character.
 */

void _puts(char *str)
{
	printf("%s\n", str);
}
