/**
 * add - Adds two integers.
 * @a: The first integer to be added.
 * @b: The second integer to be added.
 *
 * This function takes two integer values, 'a' and 'b', and calculates their
 * sum. It then returns the result as an integer.
 *
 * Return: The sum of 'a' and 'b'.
 */

int add(int a, int b)
{
	int sum = a + b;

	return (sum);
}
